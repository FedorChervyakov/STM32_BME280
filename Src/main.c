int8_t user_i2c_read(uint8_t dev_id, uint8_t reg_addr, uint8_t *reg_data, uint16_t len)
{
    int8_t rslt = BME280_OK;                    /* Return 0 for Success, non-zero for failure */

    int16_t dev_addr = (uint16_t) dev_id << 1;
    /*
     * The parameter dev_id can be used as a variable to store the I2C address of the device
     */

    /*
     * Data on the bus should be like
     * |------------+---------------------|
     * | I2C action | Data                |
     * |------------+---------------------|
     * | Start      | -                   |
     * | Write      | (reg_addr)          |
     * | Stop       | -                   |
     * | Start      | -                   |
     * | Read       | (reg_data[0])       |
     * | Read       | (....)              |
     * | Read       | (reg_data[len - 1]) |
     * | Stop       | -                   |
     * |------------+---------------------|
     */

    do 
    {
        if (HAL_I2C_Master_Transmit_DMA(&hi2c1, dev_addr, (uint8_t*) &reg_addr, 1)!= HAL_OK)
        {
            /* Error_Handler() function is called when error occurs. */
            Error_Handler();
            rslt = BME280_E_COMM_FAIL;
        }

        while (HAL_I2C_GetState(&hi2c1) != HAL_I2C_STATE_READY)
        {
        } 
        
        if(HAL_I2C_Master_Receive_DMA(&hi2c1, dev_addr, (uint8_t*) reg_data, len) != HAL_OK)
        {
            /* Error_Handler() function is called when error occurs. */
            Error_Handler();
            rslt = BME280_E_COMM_FAIL;
        }
        while (HAL_I2C_GetState(&hi2c1) != HAL_I2C_STATE_READY)
        {
        }
    }
    while (HAL_I2C_GetError(&hi2c1) == HAL_I2C_ERROR_AF);

    return rslt;
}

int8_t user_i2c_write(uint8_t dev_id, uint8_t reg_addr, uint8_t *reg_data, uint16_t len)
{
    int8_t rslt = BME280_OK;                    /* Return 0 for Success, non-zero for failure */

    uint8_t *cmd;
    int16_t dev_addr = (uint16_t) dev_id << 1;
     /*
     * The parameter dev_id can be used as a variable to store the I2C address of the device
     */

    /*
     * Data on the bus should be like
     * |------------+---------------------|
     * | I2C action | Data                |
     * |------------+---------------------|
     * | Start      | -                   |
     * | Write      | (reg_addr)          |
     * | Write      | (reg_data[0])       |
     * | Write      | (....)              |
     * | Write      | (reg_data[len - 1]) |
     * | Stop       | -                   |
     * |------------+---------------------|
     */


    /* Allocate memory for the command that is to be written on the bus*/
    cmd = (uint8_t *) malloc(sizeof(uint8_t) * len * 2);
    
    int n;
    for (int i=0; i<len; i++) {                 /* Build write command */
        n = 2*i;
        cmd[n] = reg_addr+i;                    /* register address */
        cmd[n+1] = reg_data[i];                 /* register data */
    }
    
    do
    {

        if (HAL_I2C_Master_Transmit_DMA(&hi2c1, dev_addr, (uint8_t *) cmd, len*2) != HAL_OK)    /* Send command */
        {
            Error_Handler();
            rslt = BME280_E_COMM_FAIL;
        }
        
        while (HAL_I2C_GetState(&hi2c1) != HAL_I2C_STATE_READY)
        {
        }
    }
    while (HAL_I2C_GetError(&hi2c1) == HAL_I2C_ERROR_AF);

    free(cmd);                                  /* Free buffer */
    return rslt;
}


